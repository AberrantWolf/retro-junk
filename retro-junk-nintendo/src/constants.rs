/// Decode a region from the 4th character of a Nintendo game code.
///
/// Used by GBA, DS, and 3DS cartridges where the game code follows the
/// format `XXXX` and the last character indicates region.
pub(crate) fn region_from_game_code(code: &str) -> Option<Region> {
    code.chars().nth(3).and_then(|c| match c {
        'J' => Some(Region::Japan),
        'E' => Some(Region::Usa),
        'P' | 'D' | 'F' | 'S' | 'I' | 'U' => Some(Region::Europe),
        'K' => Some(Region::Korea),
        'C' => Some(Region::China),
        'A' | 'W' => Some(Region::World),
        _ => None,
    })
}
